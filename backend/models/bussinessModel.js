import mongoose from 'mongoose';

const { Schema } = mongoose;

const businessSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    }
});

const BusinessModel = mongoose.model('Business', businessSchema);

export default BusinessModel;